using System;

namespace Chars_And_Strings
{
    public class CharsAndStrings
    {
        public static void Main()
        {
            string name = "Software University";
            char a = 'B';
            char b = 'y';
            char c = 'e';
            string love = "I love programming";
            Console.WriteLine($"{name}\r\n{a}\r\n{b}\r\n{c}\r\n{love}");
        }
    }
}
